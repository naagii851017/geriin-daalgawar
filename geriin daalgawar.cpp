#include <iostream>
#include <string>
using namespace std;
 
int main () {
  // Uildel
  int myNum = 5;               // Int
  float myFloatNum = 5.99;     // Float
  double myDoubleNum = 9.98;   // Double
  char myLetter = 'D';         // Char
  bool myBoolean = true;       // Bool
  string myString = "Sain uu";   // String
   
  // Haruulah
  cout << "int: " << myNum << "\n";
  cout << "float: " << myFloatNum << "\n";
  cout << "double: " << myDoubleNum << "\n";
  cout << "char: " << myLetter << "\n";
  cout << "bool: " << myBoolean << "\n";
  cout << "string: " << myString << "\n";
 
  return 0;
}
